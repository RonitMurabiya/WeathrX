export default function App() {
  const [city, setCity] = useState('');
  const [weather, setWeather] = useState(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [darkMode, setDarkMode] = useState(false);
  const [suggestions, setSuggestions] = useState([]);

  useEffect(() => {
    const saved = localStorage.getItem('darkMode');
    if (saved === 'true') setDarkMode(true);
  }, []);

  useEffect(() => {
    localStorage.setItem('darkMode', darkMode);
  }, [darkMode]);

  const fetchWeather = async () => {
    setLoading(true);
    setError('');
    try {
      const apiKey = import.meta.env.VITE_WEATHER_API_KEY;
      const response = await axios.get(
        `https://api.openweathermap.org/data/2.5/weather?q=${city}&units=metric&appid=${apiKey}`
      );
      setWeather(response.data);
    // eslint-disable-next-line no-unused-vars
    } catch (err) {
      setError('City not found!');
    }
    setLoading(false);
  };

  const handleInputChange = (e) => {
    const value = e.target.value;
    setCity(value);
    if (value.length > 0) {
      const filtered = citySuggestions.filter((c) =>
        c.toLowerCase().startsWith(value.toLowerCase())
      );
      setSuggestions(filtered);
    } else {
      setSuggestions([]);
    }
  };

  const handleSuggestionClick = (suggestion) => {
    setCity(suggestion);
    setSuggestions([]);
  };

  return (
    <div className={`${darkMode ? 'dark' : ''}`}>
      <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-blue-200 to-indigo-300 dark:from-gray-800 dark:to-gray-900 transition-colors duration-500 p-6">
        <div className="bg-white dark:bg-gray-800 shadow-2xl rounded-2xl p-8 max-w-md w-full space-y-6">
          <div className="flex justify-between items-center">
            <h1 className="text-3xl font-extrabold tracking-tight text-gray-900 dark:text-white">WeathrX</h1>
            <button
              onClick={() => setDarkMode(!darkMode)}
              className="p-2 rounded-full bg-gray-200 dark:bg-gray-700 hover:scale-110 transition-transform"
            >
              {darkMode ? <Sun className="text-yellow-400" /> : <Moon className="text-gray-800" />}
            </button>
          </div>

          <div className="relative">
            <div className="flex items-center gap-2">
              <input
                type="text"
                placeholder="Enter city..."
                value={city}
                onChange={handleInputChange}
                className="flex-grow px-4 py-2 rounded-lg bg-gray-100 dark:bg-gray-700 text-gray-900 dark:text-white border-none outline-none"
              />
              <button
                onClick={fetchWeather}
                className="p-2 bg-blue-500 hover:bg-blue-600 text-white rounded-lg"
              >
                <Search size={20} />
              </button>
            </div>
            {suggestions.length > 0 && (
              <ul className="absolute z-10 mt-2 w-full bg-white dark:bg-gray-700 rounded-lg shadow-lg overflow-hidden">
                {suggestions.map((s, index) => (
                  <li
                    key={index}
                    onClick={() => handleSuggestionClick(s)}
                    className="px-4 py-2 hover:bg-gray-200 dark:hover:bg-gray-600 cursor-pointer"
                  >
                    {s}
                  </li>
                ))}
              </ul>
            )}
          </div>

          {loading && <p className="text-center text-gray-500 dark:text-gray-300">Loading...</p>}
          {error && <p className="text-center text-red-500">{error}</p>}

          {weather && (
            <div className="text-center">
              <h2 className="text-2xl font-semibold text-gray-800 dark:text-white">{weather.name}</h2>
              <p className="capitalize text-lg text-gray-600 dark:text-gray-300">{weather.weather[0].description}</p>
              <p className="text-5xl font-bold text-blue-600 dark:text-blue-400">{weather.main.temp}Â°C</p>
              <img
                src={`https://openweathermap.org/img/wn/${weather.weather[0].icon}@2x.png`}
                alt="weather icon"
                className="mx-auto mt-4"
              />
              <div className="mt-4 grid grid-cols-2 gap-4 text-sm text-gray-700 dark:text-gray-300">
                <div className="bg-gray-100 dark:bg-gray-700 p-3 rounded-lg">
                  <p className="font-medium">Humidity</p>
                  <p>{weather.main.humidity}%</p>
                </div>
                <div className="bg-gray-100 dark:bg-gray-700 p-3 rounded-lg">
                  <p className="font-medium">Wind</p>
                  <p>{weather.wind.speed} m/s</p>
                </div>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}
